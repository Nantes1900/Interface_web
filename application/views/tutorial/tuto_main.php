<h2>Bienvenue sur le site du projet Nantes 1900</h2>
<p>
    Cette section a pour but d'expliquer plus en détails le fonctionnement des différentes parties du site. 
</p>
<p>
    Une fois connecté, vous avez accès au menu principal d'où vous pouvez naviguer vers les différentes sections du site. 
    Sur toutes les pages, une barre latérale est disposée à gauche pour faciliter la navigation sur le site, mais attention, 
    s'en servir changera de page et annulera donc l'action en cours, ainsi, si vous n'avez pas complété et validé un formulaire
    son contenu sera perdu.
</p>

<p>
    Pour en apprendre plus sur les différentes sections du site, cliquez simplement sur celles qui vous intéressent
</p>